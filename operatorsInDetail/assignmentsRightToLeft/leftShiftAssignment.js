/* The left shift  assignment (<<=) operator performs left shift 
on the two operands and assigns the result to the left operand. */
let a = 5; // Binary representation: 00000000000000000000000000000101

a <<= 2; // Left shift by 2 positions: 00000000000000000000000000010100

/* 1. Initial value of a in binary: 00000000000000000000000000000101 
(5 in decimal). */
/* 2. The << operator performs a left shift by 2 positions, resulting 
in: 00000000000000000000000000010100 (20 in decimal). */
